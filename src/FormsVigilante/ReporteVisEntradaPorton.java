    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
//        System.out.println(jPorton);
        if(jPorton.equals("") || ((JTextField)jDateFecha.getDateEditor().getUiComponent()).getText().equals("") || 
               ((JTextField)jDateFecha.getDateEditor().getUiComponent()).getText().equals("") ){
            JOptionPane.showMessageDialog(this, "Introduzca todos los datos por favor");
        }
        Map Parametros = new HashMap();
        String dateInicio  = ((JTextField) jDateFecha.getDateEditor().getUiComponent()).getText()+" 00:00";
        String dateFinal  = ((JTextField) jDateFecha1.getDateEditor().getUiComponent()).getText()+" 00:00";
        FrmLogin log = new FrmLogin();
        
        Parametros.put("Porton", jPorton.getText());
        Parametros.put("Vigilante", log.nombres + " " + log.apellidos);
        Parametros.put("FechaInicio", dateInicio);
        Parametros.put("FechaFinal", dateFinal);
        LlamarReportes("ReporteEntradaPorton",Parametros);
    }//GEN-LAST:event_btnImprimirActionPerformed

