    private void jbtnReporteGActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnReporteGActionPerformed
        //Generar reporte
        EntradaController EC = new EntradaController();
        String nombre, apellido;
        nombre = lblNombreUsuario.getText();
        apellido = lblApellidoUsuario.getText();            
        String fechaInicio  = ((JTextField) jDateFechaInicio.getDateEditor().getUiComponent()).getText();
        String fechaFinal  = ((JTextField) jDateFechaFinal.getDateEditor().getUiComponent()).getText();
        EC.convertirPersonal(nombre, apellido);
        int idVigilante = EC.getidPersonal();
        if (fechaInicio.isEmpty() || fechaFinal.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Por favor verificar que los datos esten llenos");
        }
        else{
            Map Parametros = new HashMap();
            Parametros.put("fechaInicio",fechaInicio); 
            Parametros.put("fechaFinal", fechaFinal);
            Parametros.put("idVigilante", idVigilante);
            LlamarReportes("ReportesEntrada",Parametros);
        }
    }//GEN-LAST:event_jbtnReporteGActionPerformed

